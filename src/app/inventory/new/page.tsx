'use client';

import { useState } from 'react';
import { useRouter } from 'next/navigation';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { z } from 'zod';
import { MainLayout } from '@/components/layout/main-layout';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { useToast } from '@/components/ui/use-toast';
import { useLanguage } from '@/contexts/language-context';
import Link from 'next/link';

const createPartSchema = (t: (key: string) => string) => z.object({
  name: z.string().min(1, t('nameRequired')),
  description: z.string().optional(),
  quantity: z.string().refine((val) => !isNaN(parseInt(val)) && parseInt(val) >= 0, {
    message: t('validQuantityRequired'),
  }),
  reorderLevel: z.string().refine((val) => !isNaN(parseInt(val)) && parseInt(val) >= 0, {
    message: t('validReorderLevelRequired'),
  }),
  unitPrice: z.string().refine((val) => !isNaN(parseFloat(val)) && parseFloat(val) >= 0, {
    message: t('validPriceRequired'),
  }),
  supplier: z.string().optional(),
});

export default function NewPartPage() {
  const router = useRouter();
  const { toast } = useToast();
  const { t } = useLanguage();
  const [isLoading, setIsLoading] = useState(false);

  const partSchema = createPartSchema(t);
  type PartFormData = z.infer<typeof partSchema>;

  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<PartFormData>({
    resolver: zodResolver(partSchema),
    defaultValues: {
      quantity: '0',
      reorderLevel: '5',
      unitPrice: '0',
    },
  });

  const onSubmit = async (data: PartFormData) => {
    setIsLoading(true);
    try {
      const response = await fetch('/api/inventory', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          name: data.name,
          description: data.description,
          quantity: parseInt(data.quantity),
          reorderLevel: parseInt(data.reorderLevel),
          unitPrice: parseFloat(data.unitPrice),
          supplier: data.supplier,
        }),
      });

      if (!response.ok) {
        throw new Error('Failed to create part');
      }

      toast({
        title: t('success'),
        description: t('partCreated'),
      });
      router.push('/inventory');
    } catch (error) {
      toast({
        title: t('error'),
        description: t('partCreateFailed'),
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <MainLayout>
      <div className="space-y-6 pt-6">
        <div>
          <h1 className="text-3xl font-bold text-gray-900 dark:text-white">{t('addNewPart')}</h1>
          <p className="text-gray-600 dark:text-gray-400">{t('addNewPartToInventory')}</p>
        </div>

        <form onSubmit={handleSubmit(onSubmit)}>
          <Card>
            <CardHeader>
              <CardTitle>{t('partInformation')}</CardTitle>
              <CardDescription>{t('enterPartDetails')}</CardDescription>
            </CardHeader>
            <CardContent className="space-y-4">
              <div className="space-y-2">
                <Label htmlFor="name">{t('partName')} *</Label>
                <Input id="name" {...register('name')} placeholder="Screen Replacement" />
                {errors.name && <p className="text-sm text-red-600">{errors.name.message}</p>}
                <p className="text-xs text-gray-500">{t('skuAutoGenerated')}</p>
              </div>

              <div className="space-y-2">
                <Label htmlFor="description">{t('partDescription')}</Label>
                <textarea
                  id="description"
                  {...register('description')}
                  rows={3}
                  className="flex w-full rounded-md border border-gray-300 bg-white px-3 py-2 text-sm focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary-500 dark:border-gray-700 dark:bg-gray-800"
                  placeholder="Part description..."
                />
              </div>

              <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
                <div className="space-y-2">
                  <Label htmlFor="quantity">{t('quantity')} *</Label>
                  <Input
                    id="quantity"
                    type="number"
                    {...register('quantity')}
                    placeholder="0"
                  />
                  {errors.quantity && (
                    <p className="text-sm text-red-600">{errors.quantity.message}</p>
                  )}
                </div>

                <div className="space-y-2">
                  <Label htmlFor="reorderLevel">{t('reorderLevel')} *</Label>
                  <Input
                    id="reorderLevel"
                    type="number"
                    {...register('reorderLevel')}
                    placeholder="5"
                  />
                  {errors.reorderLevel && (
                    <p className="text-sm text-red-600">{errors.reorderLevel.message}</p>
                  )}
                </div>

                <div className="space-y-2">
                  <Label htmlFor="unitPrice">{t('unitPrice')} ($) *</Label>
                  <Input
                    id="unitPrice"
                    type="number"
                    step="0.01"
                    {...register('unitPrice')}
                    placeholder="0.00"
                  />
                  {errors.unitPrice && (
                    <p className="text-sm text-red-600">{errors.unitPrice.message}</p>
                  )}
                </div>
              </div>

              <div className="space-y-2">
                <Label htmlFor="supplier">{t('supplier')}</Label>
                <Input id="supplier" {...register('supplier')} placeholder={t('supplierName')} />
              </div>
            </CardContent>
          </Card>

          <div className="flex gap-2 mt-6">
            <Button type="submit" disabled={isLoading}>
              {isLoading ? t('creating') : t('createPart')}
            </Button>
            <Link href="/inventory">
              <Button type="button" variant="outlined">
                {t('cancel')}
              </Button>
            </Link>
          </div>
        </form>
      </div>
    </MainLayout>
  );
}

