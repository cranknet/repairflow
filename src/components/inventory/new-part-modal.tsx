'use client';

import { useState } from 'react';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { z } from 'zod';
import { Button } from '@/components/ui/button';
import { Input, Textarea } from '@/components/ui/input';
import { useToast } from '@/components/ui/use-toast';
import { useLanguage } from '@/contexts/language-context';
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';

const createPartSchema = (t: (key: string) => string) => z.object({
  name: z.string().min(1, t('nameRequired')),
  description: z.string().optional(),
  quantity: z.string().refine((val) => !isNaN(parseInt(val)) && parseInt(val) >= 0, {
    message: t('validQuantityRequired'),
  }),
  reorderLevel: z.string().refine((val) => !isNaN(parseInt(val)) && parseInt(val) >= 0, {
    message: t('validReorderLevelRequired'),
  }),
  unitPrice: z.string().refine((val) => !isNaN(parseFloat(val)) && parseFloat(val) >= 0, {
    message: t('validPriceRequired'),
  }),
  supplier: z.string().optional(),
});

interface NewPartModalProps {
  isOpen: boolean;
  onClose: () => void;
  onSuccess: () => void;
}

export function NewPartModal({ isOpen, onClose, onSuccess }: NewPartModalProps) {
  const { toast } = useToast();
  const { t } = useLanguage();
  const [isLoading, setIsLoading] = useState(false);

  const partSchema = createPartSchema(t);
  type PartFormData = z.infer<typeof partSchema>;

  const {
    register,
    handleSubmit,
    formState: { errors },
    reset,
  } = useForm<PartFormData>({
    resolver: zodResolver(partSchema),
    defaultValues: {
      quantity: '0',
      reorderLevel: '5',
      unitPrice: '0',
    },
  });

  const onSubmit = async (data: PartFormData) => {
    setIsLoading(true);
    try {
      const response = await fetch('/api/inventory', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          name: data.name,
          description: data.description,
          quantity: parseInt(data.quantity),
          reorderLevel: parseInt(data.reorderLevel),
          unitPrice: parseFloat(data.unitPrice),
          supplier: data.supplier,
        }),
      });

      if (!response.ok) {
        throw new Error('Failed to create part');
      }

      toast({
        title: t('success'),
        description: t('partCreated'),
      });
      reset();
      onSuccess();
      onClose();
    } catch (error) {
      toast({
        title: t('error'),
        description: t('partCreateFailed'),
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle>{t('addNewPart')}</DialogTitle>
          <DialogDescription>{t('addNewPartToInventory')}</DialogDescription>
        </DialogHeader>

        <form onSubmit={handleSubmit(onSubmit)}>
          <div className="space-y-6 py-4">
            <Input
              id="modal-part-name"
              label={t('partName')}
              helperText={t('skuAutoGenerated')}
              errorText={errors.name?.message}
              required
              {...register('name')}
              placeholder="Screen Replacement"
              autoFocus
            />

            <Textarea
              id="modal-part-description"
              label={t('partDescription')}
              errorText={errors.description?.message}
              rows={3}
              {...register('description')}
              placeholder="Part description..."
            />

            <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
              <Input
                id="modal-part-quantity"
                label={t('quantity')}
                errorText={errors.quantity?.message}
                type="number"
                required
                {...register('quantity')}
                placeholder="0"
              />

              <Input
                id="modal-part-reorderLevel"
                label={t('reorderLevel')}
                errorText={errors.reorderLevel?.message}
                type="number"
                required
                {...register('reorderLevel')}
                placeholder="5"
              />

              <Input
                id="modal-part-unitPrice"
                label={`${t('unitPrice')} ($)`}
                errorText={errors.unitPrice?.message}
                type="number"
                step="0.01"
                required
                {...register('unitPrice')}
                placeholder="0.00"
              />
            </div>

            <Input
              id="modal-part-supplier"
              label={t('supplier')}
              errorText={errors.supplier?.message}
              {...register('supplier')}
              placeholder={t('supplierName')}
            />
          </div>

          <DialogFooter>
            <Button type="button" variant="outlined" onClick={onClose}>
              {t('cancel')}
            </Button>
            <Button type="submit" disabled={isLoading}>
              {isLoading ? t('creating') : t('createPart')}
            </Button>
          </DialogFooter>
        </form>
      </DialogContent>
    </Dialog>
  );
}

